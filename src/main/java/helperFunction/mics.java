public class mics {
    public static void main(String[] args){
        System.out.println("Application starts....");

        int top = 0;

        System.out.println("The value of ++top is " + ++top);
        System.out.println("The value of top is " +top);
        //top = 0;
        System.out.println("The value of top++ is " + top++);
        System.out.println("The value of top is " +top);
        //top = 0;
        System.out.println("The value of --top is " + --top);
        System.out.println("The value of top is " +top);
        //top = 0;
        System.out.println("The value of top-- is " + top--);
        System.out.println("The value of top is " +top);
    }
}
